CREATE OR REPLACE PROCEDURE tax(
	customer INTEGER,
	taxFee DECIMAL
)
LANGUAGE plpgsql
AS $$ --
BEGIN
	UPDATE buyer_invoice
	SET amount = amount + taxFee
	WHERE customer_id = customer;
	COMMIT;
END;
$$

CALL tax(1, 1000);

CREATE OR REPLACE PROCEDURE insurance(
	customer INTEGER,
	insuranceFee DECIMAL
)
LANGUAGE plpgsql
AS $$ --
BEGIN
	UPDATE seller_invoice
	SET amount = amount + insuranceFee
